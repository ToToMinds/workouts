    public function auth(Request $request)
    {
        if (!$request->has(['email', 'password'])) {
            abort(401);
        }
        if (Auth::attempt($request->all())) {
            return response()->json(User::where('email', '=', $request->get('email'))->first(['id', 'name', 'email', 'api_token', 'created_at', 'updated_at']));
        }
        abort(403);
    }

